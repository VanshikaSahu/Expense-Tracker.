function App() {
    const[visibility, setvisibility] = useState(false);
    const[style, setstyle] = useState();
    
    if (!visibility) {
        setstyle({display: "none"});
     }

     const toggle = () => {
        setvisibility(!visibility);
      };

    let state = states.map((s,index) =>{
            return(
                <>
                <h1 onClick = {toggle} id = {`state${index}`}>{s.name}</h1>
                <ul style={style}>
                {s.cities.map((city,index) => {
                    return(
                        <>
                        <h3 id = {`city${index}`}>{city.name}</h3>
                        {city.towns.map((town,index) =>{
                            return( <h6 id = {`town${index}`} >{town.name}</h6>);
                        })}
                        </>
                    )
                })}
                </ul>
                </>
                
            )
        });
    
    return (<div id="main" >
            {state}
            </div>);
}

export default App;